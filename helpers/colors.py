
class PixelColor:
  def __init__(self, r, g, b):
    self.r = r
    self.g = g
    self.b = b
    self.rgb = [r,g,b]

red = PixelColor(255,0,0)
orange = PixelColor(255,165,0)
yellow = PixelColor(255,255,0)
green = PixelColor(0,255,0)
blue = PixelColor(0,0,255)
indigo = PixelColor(75,0,130)
violet = PixelColor(159,0,255)
empty = PixelColor(0,0,0)
white = PixelColor(255,255,255)
 
